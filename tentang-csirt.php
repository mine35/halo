		<h4>Dokumen ini berisi deskripsi INTI-CSIRT berdasarkan RFC 2350, yaitu informasi dasar mengenai INTI-CSIRT, menjelaskan tanggung <br>jawab, layanan yang diberikan, dan cara untuk menghubungi INTI-<br>CSIRT.</h4><br>
		</p>
		<h2><b style="font-size: 40px;">1.</b> Visi</h2>
		<h5>Visi INTI-CSIRT adalah menjadi komponen utama untuk <br>tercapainya Visi Perusahaan dengan meningkatkan <br>ketahanan siber di sektor industri telekomunikasi.</h5><br>
		<h2><b style="font-size: 40px;">2.</b> Misi</h2>
		<h5>Perwujudan visi sebagaimana dituangkan di atas akan <br>dicapai melalui upaya- upaya yang terkandung dalam <br>misi INTI-CSIRT,
			yaitu : <br>1. Mengkoordinasikan dan mengkolaborasikan layanan <br>keamanan siber di lingkungan Perusahaan dan <br>pemangku kepentingan. <br>2. Membangun kemampuan dan kapasitas sumber daya <br>keamanan di sektor industri BUMN. <br>3. Membangun kerjasama dalam rangka <br>penanggulangan dan pemulihan insiden keamanan siber <br>di lingkungan sektor industri BUMN.</h5><br>
		<h2><b style="font-size: 40px;">3.</b> Konstituen</h2>
		<h5>Konstituen INTI-CSIRT meliputi seluruh pengguna yang <br>menggunakan layanan Data Center Perusahaan <br>Perseroan (Persero) PT Industri Telekomunikasi Indonesia,<br>meliputi karyawan dan anak perusahaan,yaitu PT. Inti <br>Konten Indonesia (INTENS) dan PT. Inti Pindad Mitra Sejati <br>(IPMS).</h5><br>
		<h2><b style="font-size: 40px;">4.</b> Sponsorship dan/atau Afiliasi</h2>
		<h5>Pendanaan INTI-CSIRT bersumber dari anggaran <br>pemeliharaan infrastrukutur Teknologi Informasi <br>Perusahaan Perseroan (Persero) PT Industri <br>Telekomunikasi Indonesia yang sudah dianggarkan setiap <br>tahunnya.</h5>
		<h2><b style="font-size: 40px;">5.</b> Otoritas</h2>
		<h5>Tim INTI-CSIRT memiliki kewenangan antara lain:
			<br>1. Menentukan asesmen tingkat keamanan informasi pada <br>proses bisnis yang sedang atau yang akan berlangsung;
			<br>2. Melakukan asesmen tingkat keamanan sistem informasi <br>yang dibuat secara sendiri (in-house),
			atau disewa/dibeli <br>ke pihak ketiga;
			<br>3. Melakukan pengawasan serta intervensi aktif terhadap <br>operasional sistem informasi dalam rangka pemenuhan <br>ketahanan dan keandalan siber yang menunjang tujuan <br>bisnis;
			<br>4. Merencanakan,membuat dan mengoperasikan <br>rancang bangun mekanisme pertahanan berlapis <br>siber (cyber defense-in-depth);
			<br>5. Melaksanakan program kesadaran keamanan siber <br>bersama stakeholder terkait;
			<br>6. Memiliki otoritas penuh untuk melaksanakan koordinasi <br>dan intervensi internal dan eksternal,
